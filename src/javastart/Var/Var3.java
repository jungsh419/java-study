package javastart.Var;

public class Var3 {
    public static void main(String[] args) {

        int a = 4;
        int b = 3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);

        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;

        System.out.println(sum);
        long longvar = 10000000000L;
        System.out.println(longvar);
        boolean bool = true;
        System.out.println(bool);

    }
}
